  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            xuehao : false,
            xingming : false,
            yuanxi : false,
            banji : false,
            sushehao : false,
            chuangweihao : false,
            wanguiyuanyin : false,
            wanguishijian : false,
            chulicuoshi : false,
            dengjiren : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          xuehao: '',
          xingming: '',
          yuanxi: '',
          banji: '',
          sushehao: '',
          chuangweihao: '',
          wanguiyuanyin: '',
          wanguishijian: '',
          chulicuoshi: '',
          dengjiren: '',
        },
        xuehaoOptions: [],
        rules: {
          xuehao: [
          ],
          xingming: [
          ],
          yuanxi: [
          ],
          banji: [
          ],
          sushehao: [
          ],
          chuangweihao: [
          ],
          wanguiyuanyin: [
          ],
          wanguishijian: [
          ],
          chulicuoshi: [
          ],
          dengjiren: [
            { required: true, message: '登记人不能为空', trigger: 'blur' },
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.wanguishijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='xuehao'){
              this.ruleForm.xuehao = obj[o];
              this.ro.xuehao = true;
              continue;
            }
            if(o=='xingming'){
              this.ruleForm.xingming = obj[o];
              this.ro.xingming = true;
              continue;
            }
            if(o=='yuanxi'){
              this.ruleForm.yuanxi = obj[o];
              this.ro.yuanxi = true;
              continue;
            }
            if(o=='banji'){
              this.ruleForm.banji = obj[o];
              this.ro.banji = true;
              continue;
            }
            if(o=='sushehao'){
              this.ruleForm.sushehao = obj[o];
              this.ro.sushehao = true;
              continue;
            }
            if(o=='chuangweihao'){
              this.ruleForm.chuangweihao = obj[o];
              this.ro.chuangweihao = true;
              continue;
            }
            if(o=='wanguiyuanyin'){
              this.ruleForm.wanguiyuanyin = obj[o];
              this.ro.wanguiyuanyin = true;
              continue;
            }
            if(o=='wanguishijian'){
              this.ruleForm.wanguishijian = obj[o];
              this.ro.wanguishijian = true;
              continue;
            }
            if(o=='chulicuoshi'){
              this.ruleForm.chulicuoshi = obj[o];
              this.ro.chulicuoshi = true;
              continue;
            }
            if(o=='dengjiren'){
              this.ruleForm.dengjiren = obj[o];
              this.ro.dengjiren = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/ruzhudengji/xuehao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.xuehaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      xuehaoChange () {
        this.$http.get('follow/ruzhudengji/xuehao?columnValue=' + this.ruleForm.xuehao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.xingming){
              this.ruleForm.xingming = res.data.data.xingming
            }
            if(res.data.data.yuanxi){
              this.ruleForm.yuanxi = res.data.data.yuanxi
            }
            if(res.data.data.banji){
              this.ruleForm.banji = res.data.data.banji
            }
            if(res.data.data.sushehao){
              this.ruleForm.sushehao = res.data.data.sushehao
            }
            if(res.data.data.chuangweihao){
              this.ruleForm.chuangweihao = res.data.data.chuangweihao
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('wanguidengji/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('wanguidengji/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('wanguidengji/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('wanguidengji/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

